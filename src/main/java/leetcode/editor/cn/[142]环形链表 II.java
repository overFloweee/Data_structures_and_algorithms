import java.util.HashSet;

/**
 * Definition for singly-linked list.
 * class ListNode {
 * int val;
 * ListNode next;
 * ListNode(int x) {
 * val = x;
 * next = null;
 * }
 * }
 */
public class Solution {
    // set
    public ListNode detectCycle1(ListNode head) {
        HashSet<ListNode> set = new HashSet<>();

        while (head != null) {
            boolean isAdd = set.add(head);
            if (!isAdd) {
                return head;
            }
            head = head.next;
        }
        return null;
    }

    // åˆ¤ç¯ç®—æ³•
    public ListNode detectCycle(ListNode head) {
        ListNode p1 = head;
        ListNode p2 = head;

        while (p2 != null && p2.next != null) {
            p1 = p1.next;
            p2 = p2.next.next;
            // ç›¸é‡äº†
            if (p1 == p2) {
                ListNode ptr = head;
                while (ptr != p1) {
                    p1 = p1.next;
                    ptr = ptr.next;
                }
                return ptr;
            }
        }
        return null;
    }

    public ListNode detectCycle(ListNode head) {
        ListNode p1 = head;
        ListNode p2 = head;
        while(p2 != null && p2.next != null)
        {
            p1 = p1.next;
            p2 = p2.next.next;
            if (p1 == p2)
            {
                p2 = head;
                while (p1 != p2)
                {
                    p1 = p1.next;
                    p2 = p2.next;
                }
                return p1;
            }
        }
        return null;
    }
}
